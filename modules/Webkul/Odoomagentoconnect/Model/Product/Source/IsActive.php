namespace Webkul\Odoomagentoconnect\Model\Product\Source;

/**
 * Class Webkul Odoomagentoconnect Product Model Source
 */
class IsActive implements \Magento\Framework\Data\OptionSourceInterface
{
    /**
     * @var \Webkul\Odoomagentoconnect\Model\IsActive
     */
    protected $_product;

    /**
     * Constructor
     *
     * @param \Webkul\Odoomagentoconnect\Model\Product $product
     */
    public function __construct(\Webkul\Odoomagentoconnect\Model\Product $product)
    {
        $this->_product = $product;
    }

    /**
     * Get options
     *
     * @return array
     */
    public function toOptionArray()
    {
        $options[] = ['label' => '', 'value' => ''];
        $availableOptions = $this->_product->getAvailableStatuses();
        foreach ($availableOptions as $key => $value) {
            $options[] = [
                'label' => $value,
                'value' => $key,
            ];
        }
        return $options;
    }
}
